import pandas as pd
import numpy as np
from sklearn.preprocessing import MinMaxScaler
from sklearn.metrics import confusion_matrix, roc_auc_score, roc_curve, classification_report
from sklearn.decomposition import PCA
from tensorflow.keras.models import Model
from tensorflow.keras.layers import Input, Dense
from tensorflow.keras.optimizers import Adam
import matplotlib.pyplot as plt
import psutil

# =============================
# 1. Load datasets
# =============================
train = pd.read_csv("train.csv")
test = pd.read_csv("test.csv")

print("Total train samples:", len(train))
print("Total test samples:", len(test))

# =============================
# 2. Prepare TRAIN data (BENIGN ONLY)
# =============================
benign_train = train[train[' Label'] == 'BENIGN'].copy()

# Drop label column
X_train = benign_train.drop(columns=[' Label'])

# Keep numeric columns only
X_train = X_train.select_dtypes(include=[np.number])

# Clean data
X_train = X_train.replace([np.inf, -np.inf], np.nan).dropna()

print("Training samples after cleaning:", X_train.shape)

# =============================
# 3-1. Feature Distribution BEFORE Scaling
# =============================
features_to_plot = X_train.columns[:5]  # first 5 features for clarity
plt.figure(figsize=(12, 6))
for col in features_to_plot:
    plt.hist(X_train[col], bins=100, alpha=0.6, label=col)
plt.title("Feature Distributions BEFORE Scaling")
plt.xlabel("Feature Value")
plt.ylabel("Frequency")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

# Scale
scaler = MinMaxScaler()
X_train_scaled = scaler.fit_transform(X_train).astype("float32")

# =============================
# 3-2. Feature Distribution AFTER Scaling
# =============================
X_train_scaled_df = pd.DataFrame(X_train_scaled, columns=X_train.columns)
plt.figure(figsize=(12, 6))
for col in features_to_plot:
    plt.hist(X_train_scaled_df[col], bins=100, alpha=0.6, label=col)
plt.title("Feature Distributions AFTER Scaling")
plt.xlabel("Scaled Feature Value")
plt.ylabel("Frequency")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

"""## 2. Building the Autoencoder."""

# =============================
# 4. Build Autoencoder
# =============================
input_dim = X_train_scaled.shape[1]
encoding_dim = 32

input_layer = Input(shape=(input_dim,))
encoder = Dense(128, activation="relu")(input_layer)
encoder = Dense(64, activation="relu")(encoder)
encoder = Dense(encoding_dim, activation="relu")(encoder)

decoder = Dense(64, activation="relu")(encoder)
decoder = Dense(128, activation="relu")(decoder)
decoder = Dense(input_dim, activation="sigmoid")(decoder)

autoencoder = Model(input_layer, decoder)
autoencoder.compile(
    optimizer=Adam(learning_rate=0.001),
    loss="mse"
)

autoencoder.summary()

"""## 3. Training the Model."""

# =============================
# 5. Train Autoencoder
# =============================
history = autoencoder.fit(
    X_train_scaled,
    X_train_scaled,
    epochs=50,
    batch_size=128,
    shuffle=True,
    validation_split=0.1,
    verbose=1
)

# =============================
# 6. Threshold from TRAIN data
# =============================
X_train_pred = autoencoder.predict(X_train_scaled, verbose=0)
mse_train = np.mean(np.square(X_train_scaled - X_train_pred), axis=1)

threshold = np.percentile(mse_train, 95)
print(f"\nReconstruction threshold (95th percentile): {threshold:.6f}")

"""## 4. Preparing the Test Data."""

# =============================
# 7. Prepare TEST data
# =============================
y_test = (test[' Label'] != 'BENIGN').astype(int)

X_test = test.drop(columns=[' Label'])
X_test = X_test.select_dtypes(include=[np.number])
X_test = X_test.replace([np.inf, -np.inf], np.nan)

mask = ~X_test.isna().any(axis=1)
X_test = X_test[mask]
y_test = y_test[mask]

print("Test samples after cleaning:", X_test.shape)
print("Test labels after cleaning:", y_test.shape)

X_test_scaled = scaler.transform(X_test).astype("float32")

"""## 5. Evaluation on Test Data."""

# =============================
# 8. Evaluate on TEST data
# =============================
X_test_pred = autoencoder.predict(X_test_scaled, verbose=0)
mse_test = np.mean(np.square(X_test_scaled - X_test_pred), axis=1)

y_pred = (mse_test > threshold).astype(int)

"""## 6. Reconstruction Error Distribution."""

# =============================
# 9. Reconstruction Error Distribution
# =============================
plt.figure(figsize=(10, 6))
plt.hist(mse_train, bins=100, alpha=0.6, label="Train (Benign)", density=True)
plt.hist(mse_test, bins=100, alpha=0.6, label="Test (Benign + Attacks)", density=True)
plt.axvline(threshold, color="red", linestyle="--", linewidth=2, label="Threshold (95th percentile)")
plt.title("Reconstruction Error Distribution")
plt.xlabel("Reconstruction Error (MSE)")
plt.ylabel("Density")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

"""## 7. Benign vs Attack Reconstruction Error Boxplot."""

# =============================
# 10. Benign vs Attack Reconstruction Error Boxplot
# =============================
mse_test_benign = mse_test[y_test == 0]
mse_test_attack = mse_test[y_test == 1]

plt.figure(figsize=(8, 6))
plt.boxplot(
    [mse_test_benign, mse_test_attack],
    labels=["Benign", "Attack"],
    showfliers=False
)
plt.axhline(threshold, color="red", linestyle="--", label="Threshold")
plt.title("Reconstruction Error Comparison")
plt.ylabel("Reconstruction Error (MSE)")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

"""## 8. Latent Space Visualization (PCA)."""

encoder_model = Model(inputs=autoencoder.input, outputs=encoder)
X_test_encoded = encoder_model.predict(X_test_scaled, batch_size=1024, verbose=0)

pca = PCA(n_components=2)
X_test_pca = pca.fit_transform(X_test_encoded)

plt.figure(figsize=(8, 6))
plt.scatter(X_test_pca[y_test == 0, 0], X_test_pca[y_test == 0, 1], s=2, alpha=0.4, label="Benign")
plt.scatter(X_test_pca[y_test == 1, 0], X_test_pca[y_test == 1, 1], s=2, alpha=0.4, label="Attack")
plt.title("Latent Space Representation (PCA)")
plt.xlabel("Principal Component 1")
plt.ylabel("Principal Component 2")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

"""## 9. Confusion Metrics."""

# =============================
# 11. Metrics
# =============================
print("\nROC-AUC:", roc_auc_score(y_test, mse_test))
print("\nConfusion Matrix:")
print(confusion_matrix(y_test, y_pred))
print("\nClassification Report:")
print(classification_report(y_test, y_pred, digits=4))

"""## 10. ROC Curve (Publication Quality)."""

# =============================
# 12. ROC Curve (Publication Quality)
# =============================
fpr, tpr, _ = roc_curve(y_test, mse_test)

plt.figure(figsize=(6, 5), dpi=200)
plt.plot(fpr, tpr, label=f"AUC = {roc_auc_score(y_test, mse_test):.4f}")
plt.plot([0, 1], [0, 1], linestyle="--", color="gray")
plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curve (Autoencoder IDS)")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

"""## 11. Calulating training loss."""

# =============================
# 10. Training Loss
# =============================
plt.figure(figsize=(6, 5))
plt.plot(history.history["loss"], label="Train Loss")
plt.plot(history.history["val_loss"], label="Validation Loss")
plt.xlabel("Epochs")
plt.ylabel("MSE Loss")
plt.title("Autoencoder Training Loss")
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()

"""## 12. Saving the trained Model."""

import joblib
# =============================
# 10. Save Model and Scaler
# =============================
print("\n💾 Saving model and scaler...")
autoencoder.save("autoencoder_model.h5")
joblib.dump(scaler, "scaler.pkl")
print("✅ Model and scaler saved successfully!")

